EncoderVel enc_left;
EncoderVel enc_right;

PI_Vel piL, piR;

static const uint16_t NEUTRAL_US = 1500;
static const uint16_t PWM_MIN_US = 1000;
static const uint16_t PWM_MAX_US = 2000;

// Set these so that when the wheel spins in the direction you consider "forward",
// the printed velocity is positive.
static const int MEAS_SIGN_L = +1;
static const int MEAS_SIGN_R = -1;

static const int CMD_SIGN_L = +1;
static const int CMD_SIGN_R = -1;   // flip right so both wheels roll the same robot-forward

static inline uint16_t clamp_pwm(int us)
{
    if (us < (int)PWM_MIN_US) return PWM_MIN_US;
    if (us > (int)PWM_MAX_US) return PWM_MAX_US;
    return (uint16_t)us;
}

static inline void set_pwm_left_us(uint16_t us)
{
    __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, us);
}

static inline void set_pwm_right_us(uint16_t us)
{
    __HAL_TIM_SET_COMPARE(&htim4, TIM_CHANNEL_1, us);
}

int _write(int file, char *ptr, int len)
{
    (void)file;
    HAL_UART_Transmit(&huart2, (uint8_t*)ptr, len, HAL_MAX_DELAY);
    return len;
}

