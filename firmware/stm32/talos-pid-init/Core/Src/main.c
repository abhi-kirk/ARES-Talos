#include "main.h"

TIM_HandleTypeDef htim1;
TIM_HandleTypeDef htim4;

UART_HandleTypeDef huart2;

void SystemClock_Config(void);
static void MX_GPIO_Init(void);
static void MX_USART2_UART_Init(void);
static void MX_TIM1_Init(void);
static void MX_TIM4_Init(void);

int main(void)
{
  HAL_Init();

  SystemClock_Config();

  MX_GPIO_Init();
  MX_USART2_UART_Init();
  MX_TIM1_Init();
  MX_TIM4_Init();

  HAL_TIM_Base_Start(&htim1);
  HAL_TIM_Base_Start(&htim4);

  HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_1);
  HAL_TIM_PWM_Start(&htim4, TIM_CHANNEL_1);

  __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, 1600);  // 1500us pulse -> 7.5% duty cycle
  __HAL_TIM_SET_COMPARE(&htim4, TIM_CHANNEL_1, 1600);

  while (1)
  {
	HAL_GPIO_TogglePin(GPIOA, GPIO_PIN_5);   // or use GPIOA, GPIO_PIN_5 if no label
	HAL_Delay(500);                               // 500 ms = ~1 Hz blink
  }
}

