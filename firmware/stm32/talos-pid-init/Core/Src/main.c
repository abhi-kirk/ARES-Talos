int main(void)
{
    HAL_Init();
    SystemClock_Config();

    MX_GPIO_Init();
    MX_USART2_UART_Init();
    MX_I2C1_Init();
    MX_TIM1_Init();
    MX_TIM4_Init();
    MX_TIM2_Init();
    MX_TIM3_Init();

    // Start encoders
    HAL_TIM_Encoder_Start(&htim2, TIM_CHANNEL_ALL);
    HAL_TIM_Encoder_Start(&htim3, TIM_CHANNEL_ALL);

    // Start PWM
    HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_1);
    HAL_TIM_PWM_Start(&htim4, TIM_CHANNEL_1);

    // Neutral immediately
    set_pwm_left_us(NEUTRAL_US);
    set_pwm_right_us(NEUTRAL_US);
    HAL_Delay(200);

    // IMU init (blocks ~700 ms for chip startup)
    if (BNO055_Init(&imu, &hi2c1) == HAL_OK) {
        imu_ok = 1;
        printf("# BNO055 OK\r\n");
    } else {
        printf("# BNO055 FAIL - check wiring/address\r\n");
    }

    // Encoder params (your measured values)
    const float TICKS_PER_REV  = 659.0f;
    const float WHEEL_RADIUS_M = 0.03375f;

    EncoderVel_Init(&enc_left,  &htim3, TICKS_PER_REV, WHEEL_RADIUS_M);
    EncoderVel_Init(&enc_right, &htim2, TICKS_PER_REV, WHEEL_RADIUS_M);

    // PI gains: start conservative, but Ki should be nonzero to overcome stiction.
    // Your right wheel needs larger deadband (from sweep).
    PI_Vel_Init(&piL, 0.08f, 0.3f, 1200.0f, 200.0f, 40.0f);
    PI_Vel_Init(&piR, 0.08f, 0.3f, 1200.0f, 200.0f, 80.0f);

    // Test schedule
    float vref = 0.0f;
    uint32_t t0 = HAL_GetTick();
    uint32_t t_prev = t0;

    // Print CSV header for host-side parsers
    printf("# mcu_ts,loop_dt,pitch,gyro_y,vref,vL,vR,uL,uR,pL,pR\r\n");

    while (1)
    {
        uint32_t t_now = HAL_GetTick();
        uint32_t loop_dt = t_now - t_prev;   // ms, for jitter monitoring
        float dt = (float)loop_dt * 0.001f;
        if (dt <= 0.0f) continue;
        t_prev = t_now;

        // 0-10s: forward, 10-15s: stop, then repeat
        uint32_t elapsed = t_now - t0;
        if (elapsed < 10000)      vref = 500.0f;
        else if (elapsed < 15000) vref = 0.0f;
        else {
            t0 = t_now;
            PI_Vel_Reset(&piL);
            PI_Vel_Reset(&piR);
            EncoderVel_Reset(&enc_left);
            EncoderVel_Reset(&enc_right);
        }

        // IMU: read pitch (Euler, deg) and pitch rate (Gyro X = same axis as Euler Pitch)
        float pitch  = imu_ok ? BNO055_GetPitch(&imu)  : 0.0f;
        float gyro_x = imu_ok ? BNO055_GetGyroX(&imu)  : 0.0f;

        // Measured velocities with consistent sign
        float vL = (float)MEAS_SIGN_L * EncoderVel_UpdateTicksPerSec(&enc_left, dt);
        float vR = (float)MEAS_SIGN_R * EncoderVel_UpdateTicksPerSec(&enc_right, dt);

        // PI outputs are PWM offsets in microseconds
        float uL = PI_Vel_Step(&piL, vref, vL, dt);
        float uR = PI_Vel_Step(&piR, vref, vR, dt);

        // Apply to PWM
        uint16_t pL = clamp_pwm((int)NEUTRAL_US + (int)(CMD_SIGN_L * uL));
        uint16_t pR = clamp_pwm((int)NEUTRAL_US + (int)(CMD_SIGN_R * uR));

        set_pwm_left_us(pL);
        set_pwm_right_us(pR);

        // Log ~20 Hz (every 5th iteration at ~5 ms loop = 100 ms period)
        static int decim = 0;
        if (++decim >= 5) {
            decim = 0;
            printf("%lu,%lu,%.2f,%.2f,%.2f,%.2f,%.2f,%.2f,%.2f,%u,%u\r\n",
                   t_now,
                   loop_dt,
                   pitch,
                   gyro_x,
                   vref,
                   vL,
                   vR,
                   uL,
                   uR,
                   pL,
                   pR);
        }

        HAL_Delay(5);
    }
}


